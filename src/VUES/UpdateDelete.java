    public UpdateDelete(String i, String n, String p, String a, String an) {
        this();
        id = Integer.parseInt(i);
        nom.setText(n);
        prenom.setText(p);
        age.setText(a);
        //get filieres values
        ResultSet res = gst.annees();
        try {
            while (res.next()) {
                annee.addItem(res.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDelete.class.getName()).log(Level.SEVERE, null, ex);
        }
        annee.setSelectedItem(an);
    }

