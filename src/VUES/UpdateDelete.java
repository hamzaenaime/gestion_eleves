    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String n = nom.getText();
        String p = prenom.getText();
        int a = Integer.parseInt(age.getText());
        String an = annee.getSelectedItem().toString();
        gst.updateEleve(id, n, p, a, an);
        ac.afficher();
        ac.setVisible(true);
        dispose();
        javax.swing.JOptionPane.showMessageDialog(this, "updated !!");
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        javax.swing.JOptionPane dialog = new javax.swing.JOptionPane();
        int choix = dialog.showConfirmDialog(this, "do you want to delete");
        //choix 0=yes 1=no 2=annuler
        if (choix == 0) {
            gst.deleteEleve(id);
            ac.afficher();
            ac.setVisible(true);
            dispose();
            javax.swing.JOptionPane.showMessageDialog(this, "deleted !!");
        } else {
            dialog.setVisible(false);
        }
    }//GEN-LAST:event_deleteActionPerformed

